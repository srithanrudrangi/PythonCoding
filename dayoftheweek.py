day = int(input('Pick a number 1 through 7:'))

if(day == 1):
    print('It is Monday')
elif(day == 2):
    print('It is Tuesday')
elif(day == 3):
    print('It is Wednesday')
elif(day == 4):
    print('It is Thursday')
elif(day == 5):
    print('It is Friday')
elif(day == 6):
    print('It is Saturday')
elif(day == 7):
    print('It is Sunday')
else:
    print('invalid input please type 1 to 7 only.')
